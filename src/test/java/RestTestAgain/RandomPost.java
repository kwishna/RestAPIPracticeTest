package RestTestAgain;

/**
 * This Class Is Created For Posting Object In db.json File. Running On Server http://localhost:3000
 * @author AB D
 *
 */
public class RandomPost {
	
	private int id;
	private String title;
	private String author;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	
	
}
